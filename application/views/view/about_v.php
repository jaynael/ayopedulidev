<p>Ayopeduli.com adalah website Social Collaboration yang mengumpulkan informasi khusus mengenai&nbsp;<em>aksi sosial</em>.Kami mempunyai Visi " Memberikan solusi sebagai Social Collaboration media dalam penggalangan dana sosial secara kredible dan transparan &nbsp;" . Kami membantu memberikan ruang khusus untuk informasi mengenai permohonan bantuan sosial yang tentunya dengan data yang tervalidasi dan dapat dipertanggung &nbsp;jawabkan. Fokus kami di bidang sosial di karenakan untuk saat ini di Indonesia kurang sekali dukungan mengenai informasi &nbsp;bantuan dana sosial tersebut. Sering di dapati tanpa cek dan ricek ada bantuan sosial kemudian secara langsung kita memberikan donasi ke mereka yang kita sendiri tidak tahu benar atau tidaknya informasi tersebut.</p>
<p>Kami membangun jaringan antara donatur dengan&nbsp;<em>aksi sosial</em>&nbsp;dan volunteer untuk dapat berkolaborasi mewujudkan hal yang lebih baik, keadaan yang lebih baik dan berimpact positif terhadap lingkungan yang mendapatkan bantuan donasi tersebut. Dengan semangat berbagi kami ingin memberikan solusi bagi siapapun yang memerlukan bantuan sosial dengan adanya kepercayaan yang terbangun.</p>
<p>Misi dari ayopeduli.com adalah</p>
<p><strong>1. COLLECT :</strong>&nbsp;Kami mengumpulkan informasi mengenai aksi sosial yang terbagi menjadi 3 kategori yaitu<br>
a. Kesehatan : aksi sosial yang berhubungan dengan Kesehatan.<br>
b. Pendidikan : aksi sosial yang berhubungan dengan Pendidikan<br>
c. Lingkungan : aksi Sosial yang behubungan dengan Lingkungan.</p>
<p><strong>2. OPEN :</strong>&nbsp;Kami mengajak pengunjung ayopeduli.com untuk lebih peduli terhadap sekililing kita untuk :<br>
a. Open Eye : Membuka mata dan memberikan gambaran yang nyata mengenai kegiatan sosial yang menjadi aksi sosial<br>
b. Open Mind : &nbsp;Membuka pikiran pengguna agar termotivasi untuk lebih peduli.<br>
c. Open Heart : Mengetuk hati pengguna untuk meningkatkan empati dalam kepedulian sesama.<br>
d. Open Wallet : Memberikan dukungan berupa materi maupun non materi untuk mewujudkan sesuatu yang lebih baik bagi "aksi sosial".</p>
<p><strong>3. Distribute :</strong>&nbsp;Kami mendistribusikan hasil donasi kepada setiap aksi sosial sepenuhnya tanpa potongan operational. sebagai wujud dari semangat Sosial Entrepreneurship sehingga kami ingin dapat berkontribusi untuk kemajuan bangsa Indonesia.</p>
