                    		<p><strong>Apa itu Ayopeduli.com ?</strong><br>
                            
Ayopeduli.com adalah Social Collaboration Media berupa website dimana anda dapat Membuat, membantu dan bergabung menjadi vounteer dalam Aksi Sosial.</p>
<p><strong>Apa Visi &nbsp;Ayopeduli.com ?</strong><br>
visi kami adalah menjadi media solusi untuk membantu menghubungkan kebaikan dalam Aksi sosial.</p>
<p><strong>Apa Misi &nbsp;Ayopeduli.com ?</strong><br>
kami mempunyai Misi<br>
- Collect<br>
- Open,<br>
- Distribute, untuk mengetahu lebih lanjut tentang visi dan misi kami silahkan buka di halaman <a href="/about" target="_blank">About Us</a></p>
<p><strong>Apa itu aksi sosial?</strong><br>
Ayopeduli menyebutnya aksi sosial yaitu aksi-aksi yang berorientasi bukan kepada profit namun lebih kepada manfaat terhadap masyarakat serta object yang kita bantu berikan donasi.</p>
<p><strong>Aksi sosialnya itu apa saja?</strong><br>
Kami membagi aksi sosial dalam 3 kategori :<br>
1. Kesehatan : adalah aksi sosial yang berhubungan dengan kesehatan, seperti perawatan, dan penyembuhan pasien.<br>
2. Pendidikan: adalah aksi sosial yang berhubungan dengan pendidikan seperti, pengadaan Perlengkapan sekolah, beasiswa, maupun kegiatan belajar mengajar..<br>
3. Lingkungan : adalah aksi sosial yang berhubungan dengan lingkungan seperti Aksi penyelamatan lingkungan, satwa dan sejenisnya.</p>
<p><span style="font-size: 1em;"><strong>Apa keuntungan berdonasi di ayopeduli.com?</strong><br>
</span>pada ayopeduli.com donasi kami bersifat transparan sebagai terima kasih kami,anda akan mendapatkan gudness poin setiap anda melakukan donasi</p>

<p><span style="font-size: 1em;"><strong>Apa itu Gudness Poin?</strong><br>
</span>Gudness poin merupakan tanda terima kasih kami kepada anda yang telah melakukan </p>
<p><span style="font-size: 1em;"><strong>Bagaimana caranya saya mendapatkan Gudness Poin?</strong><br />
</span>Ada beberapa cara untuk mendapatkan Gudness Poin :<br />
1. Register, Dengan mendaftarakan akun anda, anda akan mendapatkan 25 Gudness poin<br />
2. Menjadi Volunteer dengan di masing-masing aksi anda akan mendapatkan poin 10 Gudness poin<br />
3. Donasi, Anda akan mendapatkan Gudness poin dengan kelipatan per Rp.1.200/1 Gudness Poin
<br />
Nilai Gudness poin dapat berubah sewaktu-waktu tanpa pemberitahuan terlebih dahulu.</p>
<p><span style="font-size: 1em;"><strong>Lalu untuk apa Gudness Poin?</strong><br />
</span>Jika Anda telah mengumpulkan Gudness Poin, anda dapat meredeem poin tersebut dengan merchandise dari ayopeduli.com ataupun dengan product atau jasa layanan dari partner ayopeduli.com.</p>
<p><span style="font-size: 1em;"><strong>Apa bila saya ingin jadi partner ayopeduli.com bagaimana caranya?</strong><br />
</span>Anda dapat menghubungi bagian marketing kami atau melalui email gufron@ayopeduli.com untuk mekanisme pendaftaran menjadi partner ayopeduli.com</p>

<p><strong>Apakah donasi kami kepada aksi sosial akan dipotong untuk &nbsp;operasional website?</strong><br>
Semua hasil donasi akan diberikan 100% kepada aksi sosial.dan kami tidak berhak untuk memotong sebagai operasional website ayopeduli.com</p>
<p><span style="font-size: 1em;"><strong>Bagaimana caranya membuat aksi sosial?</strong><br>
</span>Anda dapat mengirimkan informasi aksi sosial pada pada menu home anda sesudah login atau di halaman <a href="/aksi/create">Buat Aksi</a>. dan mengisi form-form yang tersedia kemudian tunggu team kami menghubungi anda.</p>
<p><strong>Apakah ada persyaratan validasi aksi?<br>
</strong>Ya, kami mempunyai hak memvalidasi aksi untuk menerima dan menolak aksi yang tidak sesuai dengan kriteria ayopeduli.com</p>
<p><strong>Apa saja kriteria untuk aksi sosial?<br>
</strong>kami menerapkan standart kriteria aksi sosial yaitu:<br>
1. Sosial Oriented : aksi harus bersifat sosial untuk membantu dan menolong masyarakat dan individu yang memerlukan bantuan sosial.<br>
2. Sosial Impact : aksi harus mempunyai sosial impact terhadap yang dibantu ataupun masyarakat secara tidak langsung.<br>
3. Valid : Data aksi yang di inputkan merupakan data yang valid dan bukan rekayasa. serta dapat dipertanggung jawabkan.</p>
<p><strong>Jika sipenerima donasi meninggal dunia, kemanakah donasi tersebut diberikan?<br>
</strong>Jika donasi melebihi Rp.5.000.000 maka seluruh hasil donasi akan diberikan kepada ahli waris penerima donasi sebagai belasungkawa, namun jika hasil donasinya melebihi Rp.5.000.000 , maka selebihnya akan dibagi ratakan dengan penerima donasi lainnya.</p>
<p><strong>Sejauh mana peran ayopeduli.com ini untuk program sosial?</strong><br>
Ayopeduli.com BERTANGGUNG JAWAB sebagai media penampung donasi selama aksi sosial tersebut membutuhkan bantuan dan tervalidasi oleh kami . dan ayopeduli.com TIDAK BERTANGGUNG JAWAB setelah hasil donasi tersebut diberikan kepada fasilitator aksi sosial.</p>
